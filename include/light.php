<div class=psalm>   
    <h4>O Gracious Light &nbsp;&nbsp;&nbsp;&nbsp;<em>Phos hilaron</em></h4>

        <p>O gracious light, <br>
        pure brightness of the everliving Father in heaven, <br>
        O Jesus Christ, holy and blessed!<br></p> 
        
        <p>Now as we come to the setting of the sun, <br>
        and our eyes behold the vesper light, <br>
        we sing your praises, O God: Father, Son, and Holy Spirit.<br></p>
        
        <p>You are worthy at all times to be praised by happy voices, <br>
        O Son of God, O Giver of Life, <br>
        and to be glorified through all the worlds.<br></p>
</div>