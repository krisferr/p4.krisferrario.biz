<h4>Confession of Sin</h4>
        <p>Dearly beloved, we have come together in the presence of</br>
        Almighty God our heavenly Father, to set forth his praise, </br>
        to hear his holy Word, and to ask, for ourselves and on behalf</br>
        of others, those things that are necessary for our life and our</br>
        salvation.  And so that we may prepare ourselves in heart and</br>
        mind to worship him, let us kneel in silence, and with</br>
        penitent and obedient hearts confess our sins, that we may</br>
        obtain forgiveness by his infinite goodness and mercy. </br></p>
        </br>
        <h5>or this</h5>
        </br>
        <p>Let us humbly confess our sins unto Almighty God. </p>
        </br>
        <h5>Silence may be kept.</h5>
        <h5>Officiant and People together, all kneeling</h5>
        </br>
        <div class=confession>
            <p>Most merciful God,</br>
            we confess that we have sinned against you</br>
            in thought, word, and deed,</br>
            by what we have done,</br>
            and by what we have left undone.<br>
            We have not loved you with our whole heart;</br>
            we have not loved our neighbors as ourselves.</br>
            We are truly sorry and we humbly repent.</br>
            For the sake of your Son Jesus Christ,</br>
            have mercy on us and forgive us;</br>
            that we may delight in your will,</br>
            and walk in your ways,</br>
            to the glory of your name.  <em>Amen.</em></p>
        </div>
        </br>
        <h5>The Priest alone stands and says*</h5>
        </br>
        <p>Almighty God have mercy on you, forgive you all your sins through our Lord Jesus Christ, strengthen you in all goodness, and by the power of the Holy Spirit keep you in eternal life.  <em class='response'>Amen.</em></p> 
        </br>
        <h5>*A deacon or lay person using the preceding form remains kneeling, and
        substitutes "us" for "you" and "our" for "your." </h5></br></br>